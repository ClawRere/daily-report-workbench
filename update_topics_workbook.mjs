const input = await FileBlob.load(filePath);
const workbook = await SpreadsheetFile.importXlsx(input);
const sheet = workbook.worksheets.getItemAt(0);

sheet.getRange(`A1:D${rows.length}`).values = rows;
sheet.getRange(`A1:D${rows.length}`).format.wrapText = true;
sheet.freezePanes.freezeRows(1);
sheet.getRange("A:A").format.columnWidthPx = 110;
sheet.getRange("B:B").format.columnWidthPx = 150;
sheet.getRange("C:C").format.columnWidthPx = 130;
sheet.getRange("D:D").format.columnWidthPx = 460;

const checkTop = await workbook.inspect({
  kind: "table",
  sheetId: sheet.name,
  range: "A1:D12",
  include: "values",
  tableMaxRows: 12,
  tableMaxCols: 4,
  maxChars: 6000,
});
console.log("=== TOP CHECK ===");
console.log(checkTop.ndjson);

const checkBottom = await workbook.inspect({
  kind: "table",
  sheetId: sheet.name,
  range: `A${rows.length - 10}:D${rows.length}`,
  include: "values",
  tableMaxRows: 12,
  tableMaxCols: 4,
  maxChars: 6000,
});
console.log("=== BOTTOM CHECK ===");
console.log(checkBottom.ndjson);

const preview = await workbook.render({
  sheetName: sheet.name,
  range: `A1:D${rows.length}`,
  scale: 1,
  format: "png",
});
await fs.writeFile(previewPath, new Uint8Array(await preview.arrayBuffer()));

const output = await SpreadsheetFile.exportXlsx(workbook);
await output.save(filePath);

console.log(`Saved workbook: ${filePath}`);
console.log(`Saved preview: ${previewPath}`);
